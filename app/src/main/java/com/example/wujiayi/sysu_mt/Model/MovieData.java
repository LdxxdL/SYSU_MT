public class MovieData {
    public String src;
    public String name;
    public String type;
    public String score;
    public String date;
    public String intro;
    public String actor;

    public MovieData(String src, String name, String type, String score, String date, String intro, String actor) {
        this.src = src;
        this.name = name;
        this.type = type;
        this.score = score;
        this.date = date;
        this. intro = intro;
        this.actor = actor;
    }
}
